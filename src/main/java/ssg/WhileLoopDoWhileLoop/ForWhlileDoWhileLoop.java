package ssg.WhileLoopDoWhileLoop;

public class ForWhlileDoWhileLoop {
    public static void main(String[] args) {
        //3 ve 8 arasindaki sayilari ayni satirda yazdiriniz

        for (int i = 3; i <=8 ; i++) {
            System.out.print(i+" ");

        }

        System.out.println();

        int a=3;
        while (a<=8){
            System.out.print(a+" ");
            a++;
        }

        System.out.println();


        int b=3;
        do {
            System.out.print(b+" ");
            b++;
        }while (b<=8);





    }
}
